const unsigned char* PACKET_GetKey(unsigned long ver)
{
    switch(ver)
    {
        case 7:
        case 8:
        case 9:
            return key_08;
        case 10:
            return key_10;
        case 11:
        case 12:
        case 13:
        case 14:
        case 15:
            return key_11;
        /*case 15:
            return key_15;*/ // absent protocol version
        case 20:
            return key_20;
        default:
            return NULL;
    }
}

void PACKET_XorByKey(unsigned char* data, unsigned long size, unsigned long protover)
{
    const unsigned char* key = PACKET_GetKey(protover);
    if(!key) return;

    uint8_t* p = data;
    for(uint32_t i = 0; i < size; ++i)
		*(p++) ^= key[(i%0x08E)%0x050];
}

void PACKET_Crypt(Packet& packet, unsigned long protover)
{
    const unsigned char* key = PACKET_GetKey(protover);
    if(!key) return;

	std::vector<uint8_t> tbuf = packet.GetBuffer();
	for(uint32_t i = 0; i < packet.GetLength(); i++)
		tbuf[i] ^= key[(i%0x08E)%0x050];
}
